        max_value = A[0]
        for i in range(1, len(A)):
            min_here = min(min(max_pre * A[i], min_pre * A[i]), A[i])
            max_here = max(max(min_pre * A[i], max_pre * A[i]), A[i])
            max_value = max(min_here, max_here) if max_value < max(min_here, max_here) else max_value
            min_pre = min_here
            max_pre = max_here
        return max_value


